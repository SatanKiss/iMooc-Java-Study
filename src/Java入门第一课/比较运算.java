package Java入门第一课;

public class 比较运算 {
    public static void main(String[] args) {
        //等于的判定 ==
        int a = 16;
        double b = 9.5;
        String str1 = "hello";
        String str2 = "imooc";
        System.out.println("a等于b：" + (a == b));
        System.out.println("a大于b：" + (a > b));
        System.out.println("a小于等于b：" + (a <= b));
        System.out.println("str1等于str2：" + (str1 == str2));
    }
}
