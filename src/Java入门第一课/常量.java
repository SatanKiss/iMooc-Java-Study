package Java入门第一课;

public class 常量 {
    public static void main(String[] args) {
        //Java入门第一课.常量 所谓常量，我们可以理解为是一种特殊的变量，它的值被设定后，在程序运行过程中不允许改变。
        //语法名：final 常量名 = 值;
        final char sex1 = '男';
        final String sex2 = "女";
        System.out.println(sex1);
        System.out.println(sex2);
    }
}
